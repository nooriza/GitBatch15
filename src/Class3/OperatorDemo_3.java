package Class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {
        int number=10;
        System.out.println(number/3);// This answer gives 3 and will not show the reminder.
        System.out.println(number%3);// The modules will do th division and also show the reminder.

        System.out.println(16/5);
        System.out.println(16%5);
        System.out.println(156%2);
        System.out.println(155%2);//We always take the module by 2 if answer is 0 its an even number otherwise
        //it is odd  number
        System.out.println(1555%5);
    }
}
