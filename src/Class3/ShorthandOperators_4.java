package Class3;

public class ShorthandOperators_4 {
    public static void main(String[] args) {
        //These are shortcuts and these shortcuts are called shorthand operators.
        int number=10;
        //number=number+10;
        number+=10;// with the help of += to add what ever value is stored in the variabl
        System.out.println(number);
        int number2=20;
        number2-=10; // Here we instead of creating another variable to subtract, we directly
        //subtract
        System.out.println(number2);
        int number3=100;
        number3/=10;
        System.out.println(number3);
    }
}
