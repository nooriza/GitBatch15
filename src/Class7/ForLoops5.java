package Class7;

public class ForLoops5 {
    public static void main(String[] args) {
        for(int i = 1; i<=10; i++){
            System.out.println("5 *"+i+"="+(5*i));
        }
        for (int y=1; y<=10; y++){
            System.out.println("2*"+y+"="+(2*y));
        }
        for(int num4=1;num4<=10;num4++){
            System.out.println("4 *"+num4+"="+(4* num4));
        }
    }
}
