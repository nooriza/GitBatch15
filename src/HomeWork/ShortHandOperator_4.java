package HomeWork;

public class ShortHandOperator_4 {
    public static void main(String[] args) {
        int number=10;
        number+=20;
        System.out.println(number);
        number*=20;
        System.out.println(number);
        number-=10;
        System.out.println(number);
        number/=9;
        number%=10;
        System.out.println(number);
        System.out.println(number);
    }
}
